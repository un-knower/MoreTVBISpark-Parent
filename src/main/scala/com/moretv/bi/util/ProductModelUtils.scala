  def getBrand(productModel:String) = {
    if(productModel == null) "NULL"
    else if(productModel.startsWith("MiBOX")) "MiBOX"
    else if(productModel.startsWith("MagicBox")) "MagicBox"
    else if(productModel.toUpperCase().startsWith("10MOONS")) "10MOONS"
